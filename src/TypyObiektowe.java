public class TypyObiektowe {


    public static void main(String[] args) {

        // liczby stałoprzecinkowe
        Byte firstNumber = 127; // 1 bajt
        Short secondNumber = 32689; // 2 bajty
        Integer thirdNumber = 32768999; //4 bajty
        Long fourthNumber = 2148000L; // 8 bajtów

        // liczby zmiennoprzecinkow
        Float fifthNumber = 4.99934F; // 4 bajty
        Double sixNumber = 3.999999D; // 8 bajtów

        // wartość logiczna
        Boolean prawda = true;
        Boolean falsz = false;

        // pojedynczy znak
        Character letter = 'A';

        // ciagi znakow
        String hello = "Hello Michal";

//        System.out.println(hello.contains("Michal"));

    }

}
