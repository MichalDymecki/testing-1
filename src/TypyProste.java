public class TypyProste {
    public static void main(String[] args) {

        // liczby stąłoprzecinkowe
        byte firstNumber; // 1 bajt -128 do 127
        short secondNumber = 32689; //  2 bajty -23768 do 32767
        int thirdNumber = 32768999; // 4 bajty
        long fourthNumber = 2148000L; // 8 bajtów -2^63

        // liczby zmiennoprzecinkowe
        float fifthNumber = 4.99934F; //4 bajty - max ok 6-7 liczb po przecinku
        double sixNumber = 3.9999999999999D; // 8 bajtów - max ok 15 cyfr po przecinku

        // wartość logiczna
        boolean prawda = true;
        boolean falsz = false;

        // pojedynczy znak
        char letter = 'A';

    }


}
